// The code below dynamically generates and displays the list of products for different categories such as 
// hoodies jumpers and tshirts.
  const productContainer = document.getElementById("productContainer");

  // This function crrates the product for each item in the provided products array
  // each product in the array includes title, color, description, price, imageSrc
  function renderProducts(products) {
    productContainer.innerHTML = " "; 
    products.forEach(([title, color, description, price, imageSrc]) => {
      const productCard = document.createElement("div");
      productCard.classList.add("product-card");
  
      productCard.innerHTML = `
        <img src="${imageSrc}" alt="${title}"/>
        <div class="product-title">${title} (${color})</div>
        <div class="product-description">${description}</div>
        <a href="itemPage.html" class="read-more-link">Read more</a>
        <div class="product-price">${price}</div>
        <button onclick="buy('${title}', '${color}', '${price}', '${imageSrc}')" class="buy-button">Buy</button>
      `;

      // here is the implementation of the read more button with session storage and navigates it to the itempage
      productCard.querySelector(".read-more-link").addEventListener("click", (event)  => {
        event.preventDefault();
        sessionStorage.setItem(
          "selectedProduct", JSON.stringify({ title, color, description, price, imageSrc })
        );
        window.location.href = "ItemPage.html";
      });
      productContainer.appendChild(productCard);
    })};

// the eventlisteners are essential here, by clicking a link it calls the renderProducts with the corresponding array.

  document.getElementById("hoodiesLink").addEventListener("click", () => {
    renderProducts(hoodies);
  });
  
  document.getElementById("jumpersLink").addEventListener("click", () => {
    renderProducts(jumpers);
  });
  
  document.getElementById("tshirtsLink").addEventListener("click", () => {
    renderProducts(tshirts);
  });

  


  // The function buy is used for when the user clicks the buy button, a pop up is shown using the alert command
  // Also by pressing the buy button it is stored inside the Local storage anf afterwards the product can be found in the cart
  function buy(title, color, price, imageSrc) {
    alert(`${title} (${color}) was added to the Cart!`);

    const product = {title, color, price, imageSrc, quantity: 1};
    let cart = JSON.parse(localStorage.getItem("cart")) || [];
    const existingProduct = cart.find((item) => item.title === title && item.color === color);
  
    
    if(existingProduct){
      existingProduct.quantity += 1;
    
    } else{
      cart.push(product);
    }
    localStorage.setItem("cart", JSON.stringify(cart));

  


  }


